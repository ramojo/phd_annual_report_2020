<h1>What are the potential outcomes of the Annual Report?</h1>
</div>
<p>The IBTPS will review the Annual Report and assign a rating of progress. Those ratings and associated requirements are shown below:<p>


          <table class="table table-striped table-bordered table-responsive col-sm-6">
            <thead>
              <tr>
                <th>Progress Rating</th>
                <th>Description</th>
                <th>Scholar’s Action Required</th>
              </tr>
            </thead>
            <tbody>
              <tr>
                <td>Satisfactory</td>
                <td>Scholar is making expected level of progress through the programme.</td>
                <td><ul><li> Scholar continues appropriate work in the programme until the next annual report cycle.</li></ul></td>
              </tr>
              <tr>
                <td>Concern</td>
                <td>Scholar is not progressing through the programme as expected. Specific reasons for this rating will be identified in the IBTPS Report.</td>
                <td><ul><li> Scholar meets with her/his primary supervisors to discuss concerns mentioned in the IBTPS report and to make plans to address those concerns.</li>
                 <li> Within 4 weeks of receipt of the IBTPS Report, the scholar must submit to CB&ID a letter explaining what she/he is doing to improve her/his performance in the programme, together with a revised work plan.</li></ul></td>
              </tr>
              <tr>
                <td>Probation</td>
                <td>Scholar is failing to meet in a significant manner one or more expectations associated with the programme. Specific reasons for this rating will be identified in the IBTPS report.</td>
