			<p>You can download an Annual Report Form here: <a href="http://cbid.icipe.org/apps/phd_annual_report_2020/resources/PhD_icipe_Annual_Report_Form_2020_RS1.docx
"><b>Annual Report Form</b></a><p>

			<p>The form consists of three parts. Part 1 is to be completed by the PhD Scholar; Part 2 by the Primary icipe Supervisor; and Part 3 by the Primary University Supervisor.</p>

			<p>The Capacity Building and Institutional Development Programme and the icipe Board of Training and Postgraduate Studies (IBTPS) will then review the report and provide feedback to assist the scholar continue to advance through the PhD programme. The IBTPS may also request a meeting with the scholar and their supervisors to discuss progress.</p>

			<p>Please note, there may be good reasons for moving at a slower pace than expected. For example, a scholar may switch supervisors, or laboratory facilities may not be available as expected. A scholar’s progress might also be delayed by major life events, e.g. the birth of a child. These reasons should be clearly explained in the scholar’s self-assessment. These reasons will be taken into account to help the IBTPS determine a rating of progress and make recommendations.</p>
			
